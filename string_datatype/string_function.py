msg = "apple is good 4 health"

#capitalize()  -->return capitalized string
a=msg.capitalize() # first letter will capitalize
print(a)

#upper()
b=msg.upper() #all letter are capital
print(b)

#lower()
c=msg.lower() #all letter are small
print(c)

#isupper()
d=msg.isupper()  #it gives boolean output  and it gives all upper(capital letter) then it true otherwise it false
print(d)

#islower()
e=msg.islower()
print(e)

msg1 = "234r5767"
