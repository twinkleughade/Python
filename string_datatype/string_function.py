f=msg1.isupper()
print(f)

#isdigit
msg3 = "Aaa Babb"
print(msg3)

g=msg3.isdigit() #all letters will alpha 
print(g)

#isalpha
h=msg3.isalpha() #all letters will alpha  or not any space there sp it is true 
print(h)


